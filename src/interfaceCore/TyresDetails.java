package interfaceCore;

public class TyresDetails {
	
	int costOfEachTyres;
	int numberOfTyres;
	int totalCost;
	
	public int getCostOfEachTyres() {
		return costOfEachTyres;
	}
	public void setCostOfEachTyres(int costOfEachTyres) {
		this.costOfEachTyres = costOfEachTyres;
	}
	public int getNumberOfTyres() {
		return numberOfTyres;
	}
	public void setNumberOfTyres(int numberOfTyres) {
		this.numberOfTyres = numberOfTyres;
	}
	public int getTotalCost() {
		return totalCost;
	}
	public void setTotalCost(int totalCost) {
		this.totalCost = totalCost;
	}
	
	

}
