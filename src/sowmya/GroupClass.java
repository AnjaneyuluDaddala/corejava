package sowmya;

public class GroupClass {
  private int age;
  private String name;
  private double salary;
  
  GroupClass (int age,String name,double salary){
	  this.age=age;
	  this.name=name;
	  this.salary=salary;
	  
  }
  public int getAge() {
	  
	  return this.age;
  }
  public void setAge(int age) {
	  this.age=age;
  }
  public String getName() {
	return name;
 }
 public void setName(String name) {
	this.name = name;
 }
  public double getSalary() {
	return salary;
 }
 public void setSalary(double salary) {
	this.salary = salary;
 }
  
  
  
	
	
}
